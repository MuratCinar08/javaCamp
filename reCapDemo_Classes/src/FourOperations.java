public class FourOperations {
    public int Addition (int num1,int num2) {
        return num1+num2;
    }
    
    public int Extraction (int num1,int num2) {
        return num1-num2;
    }

    public int Multiplication (int num1,int num2) {
        return num1*num2;
    }
    
    public int Division (int num1,int num2) {
        return num1/num2;
    }

}
    
   
