public class Conditionals {

    public static void main (String[] args) {
        int sayi = 25;
        if(sayi<25) {
            System.out.println("Sayı 25'ten küçüktür. ");
        }else if(sayi==20) {
            System.out.println("Sayı 25'e eşittir. ");
        }else{
            System.out.println("Sayı 25'ten büyüktür. ");
        }
    }
    
}